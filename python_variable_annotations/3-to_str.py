#!/usr/bin/env python3
"""
Module de conversion.

Ce module contient une fonction `to_str` qui convertit un nombre flottant
en chaîne de caractères.
"""


def to_str(n: float) -> str:
    """
    Convertit un nombre flottant en chaîne de caractères.

    Args:
        n (float): Le nombre flottant à convertir.

    Returns:
        str: La représentation en chaîne de caractères de n.
    """
    return str(n)
